class EventModel {
  final String title;
  final String category;
  final String date;
  final String location;
  final String price;
  final String description;
  final String organiser;

  EventModel({
    required this.title,
    required this.category,
    required this.date,
    required this.location,
    required this.price,
    required this.description,
    required this.organiser,
  });
}